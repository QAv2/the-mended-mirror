#!/usr/bin/env node
/* ============================================================================
   build-dossiers.js — compile data/_dossier/*.json into the sidecar
   data/mirror-dossiers.js (window.MIRROR_DOSSIERS), loaded by index.html
   after mirror-data.js. The canonical dataset is never rewritten; dossiers
   ride beside it and can be regenerated any time:  node build-dossiers.js
   ============================================================================ */
const fs = require("fs");
const path = require("path");

const DIR = path.join(__dirname, "data", "_dossier");
const OUT = path.join(__dirname, "data", "mirror-dossiers.js");
// public sidecar carries CONTENT only; `flagged` (internal <90%-sure QA hedges)
// stays in data/_dossier/*.json for the audit pass — never shipped to the site
const KEEP = ["essence", "cosmos", "practice", "canon", "reflects", "sources"];

const out = {};
let n = 0, flagged = 0;
for (const f of fs.readdirSync(DIR).sort()) {
  if (!f.endsWith(".json")) continue;
  try {
    const d = JSON.parse(fs.readFileSync(path.join(DIR, f), "utf8"));
    const id = d.id || f.replace(/\.json$/, "");
    const slim = {};
    for (const k of KEEP) if (d[k] !== undefined && d[k] !== null && String(d[k]).length) slim[k] = d[k];
    if (Array.isArray(d.flagged) && d.flagged.length) flagged++;   // counted for the audit pass, not emitted
    out[id] = slim;
    n++;
  } catch (e) {
    console.error("skip " + f + ": " + e.message);
  }
}

fs.writeFileSync(OUT,
  "/* generated by build-dossiers.js — do not hand-edit; edit data/_dossier/*.json */\n" +
  "window.MIRROR_DOSSIERS = " + JSON.stringify(out, null, 1) + ";\n");
console.log("wrote " + OUT + " · " + n + " dossiers (" + flagged + " carry flags)");
